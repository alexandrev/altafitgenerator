package com.xandrev.altafitcalendargenerator;

/**
 *
 * @author alexa_000
 */
public class TimeTrack {
    private int idxCell;
    private String time;
    private String name;

    /**
     * @return the time
     */
    public String getTime() {
        return time;
    }

    /**
     * @param time the time to set
     */
    public void setTime(String time) {
        this.time = time;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the idxCell
     */
    public int getIdxCell() {
        return idxCell;
    }

    /**
     * @param idxCell the idxCell to set
     */
    public void setIdxCell(int idxCell) {
        this.idxCell = idxCell;
    }
    
    
}
